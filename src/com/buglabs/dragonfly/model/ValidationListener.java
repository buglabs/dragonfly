package com.buglabs.dragonfly.model;

import org.eclipse.jface.viewers.ISelectionChangedListener;
import org.eclipse.jface.viewers.SelectionChangedEvent;
import org.eclipse.swt.events.ModifyEvent;
import org.eclipse.swt.events.ModifyListener;
import org.eclipse.swt.events.SelectionEvent;
import org.eclipse.swt.events.SelectionListener;
import org.eclipse.swt.widgets.Event;
import org.eclipse.swt.widgets.Listener;

/**
 * Use this listener on WizardPage controls to validate the page, load the
 * model, and update the Wizard buttons based on the validity of the entered
 * data.
 * 
 * @author Ken
 */
public class ValidationListener implements SelectionListener, ModifyListener, Listener, ISelectionChangedListener {
	private ModelWizardPage listener;

	/**
	 * @param page
	 */
	public ValidationListener(ModelWizardPage page) {
		listener = page;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.swt.events.SelectionListener#widgetSelected(org.eclipse.swt.events.SelectionEvent)
	 */
	public void widgetSelected(SelectionEvent e) {
		validate();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.swt.events.SelectionListener#widgetDefaultSelected(org.eclipse.swt.events.SelectionEvent)
	 */
	public void widgetDefaultSelected(SelectionEvent e) {
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.swt.events.ModifyListener#modifyText(org.eclipse.swt.events.ModifyEvent)
	 */
	public void modifyText(ModifyEvent e) {
		validate();
	}

	public void validate() {
		if (listener.validatePage()) {
			listener.updateModel();
			listener.setPageComplete(true);
			return;
		}

		listener.setPageComplete(false);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.swt.widgets.Listener#handleEvent(org.eclipse.swt.widgets.Event)
	 */
	public void handleEvent(Event event) {

		validate();
	}

	public void selectionChanged(SelectionChangedEvent event) {
		validate();
	}
}